const generateHTMLReport = (scan) => {
  const { websiteUrl, securityScore, summary, createdAt } = scan;

  const getScoreColor = (score) => {
    if (score >= 80) return "#22c55e";
    if (score >= 50) return "#f59e0b";
    return "#ef4444";
  };

  const scoreColor = getScoreColor(securityScore ?? 0);

  return `
  <!DOCTYPE html>
  <html>
  <head>
    <meta charset="UTF-8" />
    <style>
      body {
        font-family: Arial, sans-serif;
        padding: 40px;
        color: #1e293b;
      }

      .header {
        display: flex;
        justify-content: space-between;
        align-items: center;
        border-bottom: 2px solid #e2e8f0;
        padding-bottom: 15px;
        margin-bottom: 30px;
      }

      .logo {
        font-size: 22px;
        font-weight: bold;
        color: #2563eb;
      }

      .score-circle {
  width: 160px;
  height: 160px;
  border-radius: 50%;
  border: 12px solid ${scoreColor};
  display: flex;
  align-items: center;
  justify-content: center;
  font-size: 32px;
  font-weight: bold;
  margin: 30px 0;
  color: ${scoreColor};
  background: #f9fafb;
  box-shadow: 0 8px 20px rgba(0,0,0,0.08);
}

      .section {
        margin-top: 30px;
      }

      .section h2 {
        margin-bottom: 10px;
        border-bottom: 1px solid #e2e8f0;
        padding-bottom: 5px;
      }

      .severity {
        padding: 8px;
        margin: 6px 0;
        border-radius: 4px;
      }

      .critical { background: #fee2e2; }
      .high { background: #fef3c7; }
      .medium { background: #fef9c3; }
      .low { background: #e0f2fe; }

      .footer {
        margin-top: 50px;
        font-size: 12px;
        color: #64748b;
        border-top: 1px solid #e2e8f0;
        padding-top: 10px;
      }

      .watermark {
  margin-top: 60px;
  font-size: 13px;
  text-align: center;
  border-top: 1px solid #e5e7eb;
  padding-top: 15px;
  color : #374151;
  font-weight : 500;
}
    </style>
  </head>

  <body>
    <div class="header">
      <div class="logo">CyberShield Lite</div>
      <div>Generated: ${new Date(createdAt).toLocaleDateString()}</div>
    </div>

    <h1>Security Report</h1>
    <p><strong>Target:</strong> ${websiteUrl ?? "N/A"}</p>

    <div class="score-circle">
      ${securityScore ?? 0}%
    </div>

    <div class="section">
  <h2>Executive Summary</h2>
  <p>
    This website has achieved a security score of ${securityScore ?? 0}%.
    ${
      (securityScore ?? 0) >= 80
        ? "Overall security posture is strong with minimal risks."
        : (securityScore ?? 0) >= 50
        ? "Moderate security risks detected. Improvements recommended."
        : "High security risks detected. Immediate action required."
    }
  </p>
</div>

    <div class="section">
      <h2>Severity Breakdown</h2>
      <div class="severity critical">Critical: ${summary?.critical ?? 0}</div>
      <div class="severity high">High: ${summary?.high ?? 0}</div>
      <div class="severity medium">Medium: ${summary?.medium ?? 0}</div>
      <div class="severity low">Low: ${summary?.low ?? 0}</div>
    </div>

    <div class="section">
      <h2>Recommendations</h2>
      <ul>
        <li>Fix critical vulnerabilities immediately.</li>
        <li>Review high severity misconfigurations.</li>
        <li>Ensure SSL and security headers are properly configured.</li>
        <li>Perform regular security audits.</li>
      </ul>
    </div>

    <div class="footer">
      This report was generated by CyberShield Lite Security Platform.
    </div>

    <div class="watermark">
  Generated with CyberShield Lite (Free Plan)
</div>
  </body>
  </html>
  `;
};

module.exports = generateHTMLReport;